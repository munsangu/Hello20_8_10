package a20_6_24;

public class TEST {
	public static void main(String[] args) {
		int a; 			// a변수는 정수형
		double b;		// b변수는 실수형
		char c;			// c변수는 문자형
		boolean d;		// d변수는 논리형
		String name;	// name변수는 문자열형
		a=10;
		b=10.5;
		c='k';
		d=true;
		name="문상우";
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(name);
	}

}
